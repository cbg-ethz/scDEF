"""Trajectory and PAGA tooling utilities for scDEF."""

from typing import Optional, List, TYPE_CHECKING, Sequence, Dict, Tuple, Any
import copy
import numpy as np
import matplotlib.pyplot as plt
import scanpy as sc

if TYPE_CHECKING:
    from scdef.models._scdef import scDEF


def _layer_score_matrix(
    model: "scDEF",
    layer_idx: int,
    normalize_per_layer: bool = False,
    eps: float = 1e-12,
) -> np.ndarray:
    """Return per-cell scores for one layer from ``adata.obsm``."""
    layer_name = model.layer_names[layer_idx]
    key = f"X_{layer_name}"
    if key not in model.adata.obsm:
        raise KeyError(
            f"Missing '{key}' in adata.obsm. Run `model.annotate_adata()` first."
        )
    x = np.asarray(model.adata.obsm[key], dtype=float)
    if normalize_per_layer:
        x = x / np.clip(np.sum(x, axis=1, keepdims=True), float(eps), None)
    return x


def _node_maps(
    model: "scDEF",
) -> Tuple[Dict[str, Dict[str, Any]], Dict[int, Dict[str, int]]]:
    """Return factor-node metadata maps for filtered factors."""
    by_name: Dict[str, Dict[str, Any]] = {}
    slot_maps: Dict[int, Dict[str, int]] = {}
    for layer_idx in range(model.n_layers):
        layer_name = model.layer_names[layer_idx]
        names = list(model.factor_names[layer_idx])
        slot_maps[layer_idx] = {n: i for i, n in enumerate(names)}
        for slot_idx, name in enumerate(names):
            by_name[name] = {
                "name": name,
                "layer_idx": int(layer_idx),
                "layer_name": layer_name,
                "slot_idx": int(slot_idx),
            }
    return by_name, slot_maps


def _adjacent_layer_weights(
    model: "scDEF",
    child_layer_idx: int,
) -> np.ndarray:
    """Return filtered parent->child weight matrix for adjacent layers.

    For child layer ``k`` and parent layer ``k+1``, returns matrix of shape
    ``(n_parent_kept, n_child_kept)``.
    """
    if child_layer_idx < 0 or child_layer_idx >= model.n_layers - 1:
        raise ValueError("child_layer_idx must be in [0, n_layers-2].")
    parent_layer_idx = child_layer_idx + 1
    parent_name = model.layer_names[parent_layer_idx]
    w_full = np.asarray(model.pmeans[f"{parent_name}W"], dtype=float)
    parent_kept = np.asarray(model.factor_lists[parent_layer_idx], dtype=int)
    child_kept = np.asarray(model.factor_lists[child_layer_idx], dtype=int)
    return w_full[np.ix_(parent_kept, child_kept)]


def build_differentiation_paths(
    model: "scDEF",
    rel_parent_weight: float = 0.25,
    abs_parent_weight: float = 0.0,
    max_paths_per_leaf: int = 8,
    key_added: str = "differentiation_paths",
) -> List[List[str]]:
    """Build hierarchy-consistent differentiation paths (top->leaf).

    Paths are generated by walking from each layer-0 factor upward through
    adjacent-layer parent weights, keeping all parent links that pass
    adaptive filtering:

    ``w >= max(abs_parent_weight, rel_parent_weight * max_parent_weight_for_child)``.
    """
    if model.n_layers < 2:
        model.adata.uns[key_added] = {
            "paths": [],
            "params": {
                "rel_parent_weight": float(rel_parent_weight),
                "abs_parent_weight": float(abs_parent_weight),
                "max_paths_per_leaf": int(max_paths_per_leaf),
            },
            "fit_revision": int(getattr(model, "_fit_revision", 0)),
        }
        return []
    if rel_parent_weight < 0:
        raise ValueError("rel_parent_weight must be >= 0.")
    if abs_parent_weight < 0:
        raise ValueError("abs_parent_weight must be >= 0.")
    if int(max_paths_per_leaf) <= 0:
        raise ValueError("max_paths_per_leaf must be > 0.")

    n_layers = int(model.n_layers)
    leaf_names = list(model.factor_names[0])
    paths: List[List[str]] = []

    parent_choices: Dict[Tuple[int, int], List[int]] = {}
    edge_weights: Dict[Tuple[str, str], float] = {}
    for child_layer_idx in range(n_layers - 1):
        parent_layer_idx = child_layer_idx + 1
        w = _adjacent_layer_weights(model, child_layer_idx)
        for child_slot in range(w.shape[1]):
            col = np.asarray(w[:, child_slot], dtype=float)
            max_w = float(np.max(col)) if col.size > 0 else 0.0
            thr = max(float(abs_parent_weight), float(rel_parent_weight) * max_w)
            keep = np.where(col >= thr)[0]
            if keep.size == 0 and col.size > 0:
                keep = np.array([int(np.argmax(col))], dtype=int)
            parent_choices[(child_layer_idx, child_slot)] = [int(i) for i in keep]
            child_name = model.factor_names[child_layer_idx][child_slot]
            for p_slot in keep:
                parent_name = model.factor_names[parent_layer_idx][int(p_slot)]
                edge_weights[(parent_name, child_name)] = float(col[int(p_slot)])

    def _grow_up(child_layer_idx: int, child_slot: int, suffix: List[str]) -> None:
        if child_layer_idx == n_layers - 1:
            paths.append(list(reversed(suffix)))
            return
        options = parent_choices.get((child_layer_idx, child_slot), [])
        if len(options) == 0:
            paths.append(list(reversed(suffix)))
            return
        for p_slot in options[: int(max_paths_per_leaf)]:
            parent_name = model.factor_names[child_layer_idx + 1][int(p_slot)]
            _grow_up(child_layer_idx + 1, int(p_slot), suffix + [parent_name])

    for leaf_name in leaf_names:
        leaf_slot = model.factor_names[0].index(leaf_name)
        _grow_up(0, int(leaf_slot), [leaf_name])

    # Deduplicate while preserving order.
    seen = set()
    unique_paths: List[List[str]] = []
    for p in paths:
        t = tuple(p)
        if t not in seen:
            seen.add(t)
            unique_paths.append(p)

    path_objs = []
    for idx, p in enumerate(unique_paths):
        prod = 1.0
        for a, b in zip(p[:-1], p[1:]):
            prod *= float(edge_weights.get((a, b), 1e-12))
        path_objs.append(
            {
                "path_id": int(idx),
                "nodes": list(p),
                "type": "differentiation",
                "path_weight_product": float(prod),
            }
        )

    model.adata.uns[key_added] = {
        "paths": path_objs,
        "params": {
            "rel_parent_weight": float(rel_parent_weight),
            "abs_parent_weight": float(abs_parent_weight),
            "max_paths_per_leaf": int(max_paths_per_leaf),
        },
        "fit_revision": int(getattr(model, "_fit_revision", 0)),
    }
    return unique_paths


def build_transition_paths(
    model: "scDEF",
    rel_parent_weight: float = 0.2,
    abs_parent_weight: float = 0.0,
    max_path_len: int = 5,
    max_paths_per_pair: int = 5,
    terminal_layer_idx: int = 0,
    sources: Optional[Sequence[str]] = None,
    targets: Optional[Sequence[str]] = None,
    key_added: str = "transition_paths",
) -> List[List[str]]:
    """Build transition paths on a soft inter-layer factor graph.

    De novo mode: ``sources=None`` and ``targets=None`` -> all terminal pairs.
    Targeted mode: provide both ``sources`` and ``targets`` factor-name lists.
    """
    if terminal_layer_idx < 0 or terminal_layer_idx >= model.n_layers:
        raise ValueError("terminal_layer_idx out of bounds.")
    if int(max_path_len) < 2:
        raise ValueError("max_path_len must be >= 2.")
    if int(max_paths_per_pair) <= 0:
        raise ValueError("max_paths_per_pair must be > 0.")
    if rel_parent_weight < 0 or abs_parent_weight < 0:
        raise ValueError("weight thresholds must be >= 0.")
    if (sources is None) ^ (targets is None):
        raise ValueError("Provide both sources and targets, or neither.")

    node_meta, _ = _node_maps(model)
    terminal_nodes = list(model.factor_names[terminal_layer_idx])

    # Build undirected adjacency with edge weights from adjacent-layer W.
    adjacency: Dict[str, List[str]] = {name: [] for name in node_meta}
    edge_w: Dict[frozenset, float] = {}

    for child_layer_idx in range(model.n_layers - 1):
        parent_layer_idx = child_layer_idx + 1
        w = _adjacent_layer_weights(model, child_layer_idx)
        parent_names = list(model.factor_names[parent_layer_idx])
        child_names = list(model.factor_names[child_layer_idx])
        for child_slot in range(w.shape[1]):
            col = np.asarray(w[:, child_slot], dtype=float)
            max_w = float(np.max(col)) if col.size > 0 else 0.0
            thr = max(float(abs_parent_weight), float(rel_parent_weight) * max_w)
            keep = np.where(col >= thr)[0]
            if keep.size == 0 and col.size > 0:
                keep = np.array([int(np.argmax(col))], dtype=int)
            child = child_names[child_slot]
            for p_slot in keep:
                parent = parent_names[int(p_slot)]
                if child not in adjacency[parent]:
                    adjacency[parent].append(child)
                if parent not in adjacency[child]:
                    adjacency[child].append(parent)
                edge_w[frozenset((parent, child))] = float(col[int(p_slot)])

    if sources is None:
        endpoint_pairs = []
        for i in range(len(terminal_nodes)):
            for j in range(i + 1, len(terminal_nodes)):
                endpoint_pairs.append((terminal_nodes[i], terminal_nodes[j]))
    else:
        src = list(sources)
        tgt = list(targets) if targets is not None else []
        unknown = [n for n in src + tgt if n not in node_meta]
        if len(unknown) > 0:
            raise ValueError(f"Unknown factor names in sources/targets: {unknown}")
        endpoint_pairs = [(a, b) for a in src for b in tgt if a != b]

    all_paths: List[List[str]] = []
    path_meta: List[Dict[str, Any]] = []
    seen = set()

    def _dfs(
        current: str,
        target: str,
        visited: set,
        path: List[str],
        collected: List[List[str]],
    ) -> None:
        if len(path) > int(max_path_len):
            return
        if current == target:
            collected.append(list(path))
            return
        for nxt in adjacency.get(current, []):
            if nxt in visited:
                continue
            visited.add(nxt)
            path.append(nxt)
            _dfs(nxt, target, visited, path, collected)
            path.pop()
            visited.remove(nxt)

    for src, tgt in endpoint_pairs:
        collected: List[List[str]] = []
        _dfs(src, tgt, {src}, [src], collected)
        if len(collected) == 0:
            continue

        scored = []
        for p in collected:
            prod = 1.0
            for a, b in zip(p[:-1], p[1:]):
                prod *= float(edge_w.get(frozenset((a, b)), 1e-12))
            scored.append((prod, p))
        scored.sort(key=lambda x: x[0], reverse=True)
        keep = scored[: int(max_paths_per_pair)]

        for prod, p in keep:
            if sources is None:
                # De novo paths are undirected; deduplicate reverse duplicates.
                key = min(tuple(p), tuple(reversed(p)))
            else:
                key = tuple(p)  # preserve source->target direction in targeted mode
            if key in seen:
                continue
            seen.add(key)
            all_paths.append(p)
            path_meta.append(
                {
                    "source": str(src),
                    "target": str(tgt),
                    "path_weight_product": float(prod),
                }
            )

    path_objs = []
    for idx, (p, meta) in enumerate(zip(all_paths, path_meta)):
        path_objs.append(
            {
                "path_id": int(idx),
                "nodes": list(p),
                "type": "transition",
                **meta,
            }
        )

    model.adata.uns[key_added] = {
        "paths": path_objs,
        "params": {
            "rel_parent_weight": float(rel_parent_weight),
            "abs_parent_weight": float(abs_parent_weight),
            "max_path_len": int(max_path_len),
            "max_paths_per_pair": int(max_paths_per_pair),
            "terminal_layer_idx": int(terminal_layer_idx),
            "mode": "targeted" if sources is not None else "de_novo",
            "sources": None if sources is None else list(sources),
            "targets": None if targets is None else list(targets),
        },
        "fit_revision": int(getattr(model, "_fit_revision", 0)),
    }
    return all_paths


def score_paths(
    model: "scDEF",
    paths_key: str = "transition_paths",
    key_added: Optional[str] = None,
    normalize_per_layer: bool = True,
    min_affinity: float = 0.0,
    eps: float = 1e-12,
) -> None:
    """Score per-cell position and affinity on previously built paths.

    Writes:
      - ``adata.obsm[f"{prefix}_positions"]`` (n_cells, n_paths)
      - ``adata.obsm[f"{prefix}_affinities"]`` (n_cells, n_paths)
      - per-path summary table in ``adata.uns[paths_key]["path_stats"]``.
    """
    if key_added is None:
        key_added = str(paths_key)
    if not (0.0 <= float(min_affinity) <= 1.0):
        raise ValueError("min_affinity must be in [0, 1].")

    if paths_key not in model.adata.uns:
        raise KeyError(f"'{paths_key}' not found in adata.uns. Build paths first.")
    path_objs = list(model.adata.uns[paths_key].get("paths", []))
    if len(path_objs) == 0:
        n_cells = model.adata.n_obs
        model.adata.obsm[f"{key_added}_positions"] = np.zeros((n_cells, 0))
        model.adata.obsm[f"{key_added}_affinities"] = np.zeros((n_cells, 0))
        model.adata.uns[paths_key]["path_stats"] = []
        return

    node_meta, slot_maps = _node_maps(model)
    n_cells = int(model.adata.n_obs)
    n_paths = int(len(path_objs))

    # Cache per-layer score matrices for speed.
    layer_mats = {
        i: _layer_score_matrix(
            model,
            layer_idx=i,
            normalize_per_layer=bool(normalize_per_layer),
            eps=float(eps),
        )
        for i in range(model.n_layers)
    }
    total_mass = np.zeros(n_cells, dtype=float)
    for i in range(model.n_layers):
        total_mass += np.sum(layer_mats[i], axis=1)
    total_mass = np.clip(total_mass, float(eps), None)

    pos_mat = np.full((n_cells, n_paths), np.nan, dtype=float)
    aff_mat = np.zeros((n_cells, n_paths), dtype=float)
    stats = []

    for p_idx, pobj in enumerate(path_objs):
        nodes = list(pobj["nodes"])
        if len(nodes) == 0:
            continue

        node_scores = []
        for n in nodes:
            if n not in node_meta:
                raise ValueError(
                    f"Path node '{n}' not found among current filtered factor names."
                )
            info = node_meta[n]
            layer_idx = int(info["layer_idx"])
            slot_idx = int(slot_maps[layer_idx][n])
            node_scores.append(layer_mats[layer_idx][:, slot_idx])
        s = np.vstack(node_scores).T  # (n_cells, path_len)

        path_mass = np.sum(s, axis=1)
        affinity = np.clip(path_mass / total_mass, 0.0, 1.0)
        aff_mat[:, p_idx] = affinity

        coords = np.linspace(0.0, 1.0, num=s.shape[1], dtype=float)
        denom = np.clip(path_mass, float(eps), None)
        w = s / denom[:, None]
        pos = np.sum(w * coords[None, :], axis=1)
        pos[affinity < float(min_affinity)] = np.nan
        pos_mat[:, p_idx] = pos

        valid = np.isfinite(pos)
        mid = valid & (pos >= 0.2) & (pos <= 0.8)
        stats.append(
            {
                "path_id": int(pobj.get("path_id", p_idx)),
                "n_cells_above_min_affinity": int(
                    np.sum(affinity >= float(min_affinity))
                ),
                "n_cells_with_position": int(np.sum(valid)),
                "n_cells_mid_region": int(np.sum(mid)),
                "mean_affinity": float(np.mean(affinity)),
                "mean_position": float(np.nanmean(pos)) if np.any(valid) else np.nan,
                "position_variance": float(np.nanvar(pos)) if np.any(valid) else np.nan,
            }
        )

    model.adata.obsm[f"{key_added}_positions"] = pos_mat
    model.adata.obsm[f"{key_added}_affinities"] = aff_mat
    model.adata.uns[paths_key]["scoring"] = {
        "key_added": str(key_added),
        "normalize_per_layer": bool(normalize_per_layer),
        "min_affinity": float(min_affinity),
    }
    model.adata.uns[paths_key]["path_stats"] = stats


def multilevel_paga(
    model: "scDEF",
    neighbors_rep: str = "X_L0",
    layers: Optional[List[int]] = None,
    reuse_pos: bool = True,
    layout: str = "fa",
    random_seed: int = 0,
    **paga_kwargs,
) -> None:
    """Compute and cache multilevel PAGA results for plotting."""
    if layers is None:
        layers = [
            i
            for i in range(model.n_layers - 1, -1, -1)
            if len(model.factor_lists[i]) > 1
        ]
    if len(layers) == 0:
        model.adata.uns["multilevel_paga"] = {
            "neighbors_rep": neighbors_rep,
            "layers": [],
            "reuse_pos": reuse_pos,
            "layout": layout,
            "results": {},
        }
        return

    sc.pp.neighbors(model.adata, use_rep=neighbors_rep)
    results = {}
    pos = None
    old_layer_name = None
    old_paga = copy.deepcopy(model.adata.uns.get("paga", None))

    for layer_idx in layers:
        layer_name = model.layer_names[layer_idx]

        if old_layer_name is not None and reuse_pos:
            matches = sc._utils.identify_groups(
                model.adata.obs[layer_name], model.adata.obs[old_layer_name]
            )
            pos = []
            np.random.seed(random_seed)
            prev_pos = model.adata.uns["paga"]["pos"]
            coarse_categories = model.adata.obs[old_layer_name].cat.categories
            for c in model.adata.obs[layer_name].cat.categories:
                idx = coarse_categories.get_loc(matches[c][0])
                pos_i = prev_pos[idx] + np.random.random(2)
                pos.append(pos_i)
            pos = np.array(pos)

        sc.tl.paga(model.adata, groups=layer_name)
        sc.pl.paga(
            model.adata,
            init_pos=pos,
            layout=layout,
            show=False,
            **paga_kwargs,
        )
        plt.close()
        results[layer_name] = {
            "paga": copy.deepcopy(model.adata.uns["paga"]),
            "pos": np.array(model.adata.uns["paga"]["pos"]),
            "layer_idx": int(layer_idx),
        }
        old_layer_name = layer_name

    if old_paga is None:
        model.adata.uns.pop("paga", None)
    else:
        model.adata.uns["paga"] = old_paga

    model.adata.uns["multilevel_paga"] = {
        "neighbors_rep": neighbors_rep,
        "layers": [int(i) for i in layers],
        "reuse_pos": bool(reuse_pos),
        "layout": layout,
        "results": results,
    }
